    private void btnConsultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultActionPerformed
        // TODO add your handling code here:
        String locat = txfLocation.getText().replace(" ", "%20");
        if (txfLocation.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Â¡Ingrese una localida para continuar!");
        } else {
            api.getRequestMaps(locat);
            lat = api.lat.toString();
            lng = api.lng.toString();
            api.getRequestWeather(lat, lng);
            txfTimezone.setText(api.timeZone);
            txfTime.setText(api.time.toString());
            txfSumary.setText(api.summary);
            icon = api.icon.toString();
            lbIcon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/" + icon + ".png")));
        }

    }//GEN-LAST:event_btnConsultActionPerformed

